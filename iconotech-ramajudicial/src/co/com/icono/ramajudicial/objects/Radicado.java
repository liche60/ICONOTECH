package co.com.icono.ramajudicial.objects;

public class Radicado {

	private long idmunicipio;
	private long idjuzgado;
	private long idespecialidad;
	private long consecutivo_despacho;
	private long año;
	private long consecutivo_radicado;
	private long consecutivo_recurso;
	
	public long getIdmunicipio() {
		return idmunicipio;
	}
	public void setIdmunicipio(long idmunicipio) {
		this.idmunicipio = idmunicipio;
	}
	public long getIdjuzgado() {
		return idjuzgado;
	}
	public void setIdjuzgado(long idjuzgado) {
		this.idjuzgado = idjuzgado;
	}
	public long getIdespecialidad() {
		return idespecialidad;
	}
	public void setIdespecialidad(long idespecialidad) {
		this.idespecialidad = idespecialidad;
	}
	public long getConsecutivo_despacho() {
		return consecutivo_despacho;
	}
	public void setConsecutivo_despacho(long consecutivo_despacho) {
		this.consecutivo_despacho = consecutivo_despacho;
	}
	public long getAño() {
		return año;
	}
	public void setAño(long año) {
		this.año = año;
	}
	public long getConsecutivo_radicado() {
		return consecutivo_radicado;
	}
	public void setConsecutivo_radicado(long consecutivo_radicado) {
		this.consecutivo_radicado = consecutivo_radicado;
	}
	public long getConsecutivo_recurso() {
		return consecutivo_recurso;
	}
	public void setConsecutivo_recurso(long consecutivo_recurso) {
		this.consecutivo_recurso = consecutivo_recurso;
	}
	
	
	
}
