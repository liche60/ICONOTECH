package co.com.icono.ramajudicial.objects;

public class Actuacion {

	private String fechaActuacion;
	private String actuacion;
	private String anotacion;
	private String fechaIniciaTermino;
	private String fechaFinalizaTermino;
	private String fechaRegistro;

	public String getFechaActuacion() {
		return fechaActuacion;
	}

	public void setFechaActuacion(String fechaActuacion) {
		this.fechaActuacion = fechaActuacion;
	}

	public String getActuacion() {
		return actuacion;
	}

	public void setActuacion(String actuacion) {
		this.actuacion = actuacion;
	}

	public String getAnotacion() {
		return anotacion;
	}

	public void setAnotacion(String anotacion) {
		this.anotacion = anotacion;
	}

	public String getFechaIniciaTermino() {
		return fechaIniciaTermino;
	}

	public void setFechaIniciaTermino(String fechaIniciaTermino) {
		this.fechaIniciaTermino = fechaIniciaTermino;
	}

	public String getFechaFinalizaTermino() {
		return fechaFinalizaTermino;
	}

	public void setFechaFinalizaTermino(String fechaFinalizaTermino) {
		this.fechaFinalizaTermino = fechaFinalizaTermino;
	}

	public String getFechaRegistro() {
		return fechaRegistro;
	}

	public void setFechaRegistro(String fechaRegistro) {
		this.fechaRegistro = fechaRegistro;
	}

}
